#Arithmetic Operators
num1 = 10
num2 = 5
print(num1 + num2)
print(num1 - num2)
print(num1 * num2)
print(num1 / num2)
print(num1 % num2) #Modulus - Gives you the reminder

print()
#Comparison Operators
print(num1 < num2)#less than
print(num1 > num2)
print(num1 <= num2)
print(num1 >= num2)
print(num1 == num2)#equal to
print(num1 != num2)#Not equal to

#Assignment Operators - storing values in variables
x = 100
print(x)

x += 2 #Operator Precedence
print(x)

#Logical Operators - and, or, not
print( 300 > 200 and 50 == 20)
print( 300 > 200 or 50 == 20)
print(not(300 > 200 or 50 == 20))



